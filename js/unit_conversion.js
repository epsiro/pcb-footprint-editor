function nm_to_view(number) {
    return number/10000
}

function view_to_nm(number) {
    return number*10000
}

function nm_to_mm(number) {
    return number/1000000
}

function mm_to_nm(number) {
    return number*1000000
}

function nm_to_mil(number) {
    return number/25400
}

function mil_to_nm(number) {
    return number*25400
}
